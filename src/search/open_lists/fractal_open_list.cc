template<class Entry>
FractalOpenList<Entry>::FractalOpenList(const Options &opts)
    : OpenList<Entry>(opts.get<bool>("pref_only"),QueueType(opts.get_enum("queue_type"))),
      lowest_bucket(numeric_limits<int>::max()),
      size(0),
      evaluator(opts.get<ScalarEvaluator *>("eval")) {
}

template<class Entry>
void FractalOpenList<Entry>::do_insertion(
    EvaluationContext &eval_context, const Entry &entry) {
    int key = eval_context.get_heuristic_value(evaluator);
    assert(key >= 0);
    int num_buckets = buckets.size();
    if (key >= num_buckets)
        buckets.resize(key + 1);
    if (key < lowest_bucket)
        lowest_bucket = key;
    buckets[key].push_back(entry);
    ++size;
}

template<class Entry>
Entry FractalOpenList<Entry>::remove_min(vector<int> *key) {
    assert(size > 0);
    while (buckets[lowest_bucket].empty())
        ++lowest_bucket;
    --size;
    if (key) {
        assert(key->empty());
        key->push_back(lowest_bucket);
    }
    return pop_bucket<Entry,Bucket>(buckets[lowest_bucket], this->queue_type);
}

template<class Entry>
bool FractalOpenList<Entry>::empty() const {
    return size == 0;
}

template<class Entry>
void FractalOpenList<Entry>::clear() {
    buckets.clear();
    lowest_bucket = numeric_limits<int>::max();
    size = 0;
}

template<class Entry>
void FractalOpenList<Entry>::get_involved_heuristics(
    set<Heuristic *> &hset) {
    evaluator->get_involved_heuristics(hset);
}

template<class Entry>
bool FractalOpenList<Entry>::is_dead_end(
    EvaluationContext &eval_context) const {
    return eval_context.is_heuristic_infinite(evaluator);
}

template<class Entry>
bool FractalOpenList<Entry>::is_reliable_dead_end(
    EvaluationContext &eval_context) const {
    return is_dead_end(eval_context) && evaluator->dead_ends_are_reliable();
}

FractalOpenListFactory::FractalOpenListFactory(
    const Options &options)
    : options(options) {
}

unique_ptr<StateOpenList>
FractalOpenListFactory::create_state_open_list() {
    return Utils::make_unique_ptr<FractalOpenList<StateOpenListEntry>>(options);
}

unique_ptr<EdgeOpenList>
FractalOpenListFactory::create_edge_open_list() {
    return Utils::make_unique_ptr<FractalOpenList<EdgeOpenListEntry>>(options);
}

static shared_ptr<OpenListFactory> _parse(OptionParser &parser) {
    parser.document_synopsis(
        "Balanced Bucket-based open list",
        "Balanced Bucket-based open list implementation that uses a single evaluator. "
        "Ties are broken in FIFO order.");
    parser.add_option<ScalarEvaluator *>("eval", "scalar evaluator");
    parser.add_option<bool>(
        "pref_only",
        "insert only nodes generated by preferred operators",
        "false");

    add_queue_type_option_to_parser(parser);
    Options opts = parser.parse();
    if (parser.dry_run())
        return nullptr;
    else
        return make_shared<FractalOpenListFactory>(opts);
}

static PluginShared<OpenListFactory> _plugin("fractal", _parse);
